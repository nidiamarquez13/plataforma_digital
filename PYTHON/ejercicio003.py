#Area y perimetro del triangulo 
base = float(input("Ingrese la base del Triángulo: "))
altura = float(input("Ingrese la altura del Triángulo: "))
lado = float(input("Ingrese el lado del Triángulo: "))
#Operaciones
perimetro = lado + lado + base
area = (base * altura)/2
#Resultados
print("El Perimetro del Triángulo: ", perimetro)
print("El Area del Triángulo: ", area)





